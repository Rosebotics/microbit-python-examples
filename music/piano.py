import microbit as mb
import music


def main():
    mb.display.off()
    while True:
        if mb.pin6.read_digital():
            music.play('b3')
        if mb.pin7.read_digital():
            music.play('c4')
        if mb.pin1.read_digital():
            music.play('d4')
        if mb.pin8.read_digital():
            music.play('e4')
        if mb.pin9.read_digital():
            music.play('f4')
        if mb.pin10.read_digital():
            music.play('g4')

        if mb.pin2.read_digital():
            twinkleTwinkle()
        if mb.pin13.read_digital():
            jingleBells()
        if mb.pin14.read_digital():
            marioTheme()
        if mb.pin15.read_digital():
            prelude_in_C()


